package org.example.pojo;

public class User {
    private String id;
    private String name;
    private String createTime;
    private String updateTime;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getCreateTime() {
        return createTime;
    }

    public String getUpdateTime() {
        return updateTime;
    }
}
